public class Variables {
    public static void main(String[] args){
        int myInt;
        myInt = 4;
        // целые 64 бита//
        short myShort = 44;
        // целые -32000 - +32000//
        long myLong = 95;
        // целые 64 бита //
        double myDouble = 32.88;
        //вещественные 64 бита//
        float myFloat = 44.55f;
        //вещественные 32 бита нужно f в конце, по стандарту double//
        char c = 'G';
        //символ, пишется в одинарных кавычках//
        boolean b = true;
        //логический тип данных//
        byte myByte = 3;
        // 1 байт. числа от -128 до 127//
        System.out.println(myByte);

    }
}
